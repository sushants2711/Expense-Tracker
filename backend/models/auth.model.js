import mongoose from "mongoose";

const authSchema = mongoose.Schema({
    name: {
        type: String,
        required: true,
    },
    email: {
        type: String,
        required: true,
        unique: true,
    },
    password: {
        type: String,
        required: true,
        minlength: 8,
        maxlength: 100,
    },
}, { timestamps: true });

export default mongoose.model("authSchema", authSchema);